        int selectRow = NurseRequestTable.getSelectedRow();
        if(selectRow < 0)
        {
            JOptionPane.showMessageDialog(null, "Kindly Select a row");
            return;
        }

        WorkRequest workRequest = (WorkRequest) NurseRequestTable.getValueAt(selectRow, 0);
        //Patient patient = workRequest.getPatient();
        Encounter encounter = workRequest.getEnocunter();
        //Assessment assessment = encounter.getAssessmentHistory().createAssessment();

        CollectVitalSignWorkArea cvsswa = new CollectVitalSignWorkArea(userProcessContainer,userAccount,encounter,enterprise,network,workRequest);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("CollectVitalSignWorkArea", cvsswa);
        layout.next(userProcessContainer);

