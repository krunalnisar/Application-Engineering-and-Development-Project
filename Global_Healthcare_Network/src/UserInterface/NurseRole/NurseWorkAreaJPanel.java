        public void NurseTable()
        {
            
             DefaultTableModel model = (DefaultTableModel) NurseRequestTable.getModel();
        
        model.setRowCount(0);
        for(WorkRequest wr : organization.getWorkQueue().getWorkRequestList())
        {
            Object row [] = new Object[6];
            row[0]= wr;
            row[1]=wr.getPatient().getName();
            row[2]=wr.getEnocunter();
            row[3]=wr.getSender();
            row[4]=wr.getReceiver();
            String result = ((NurseWorkRequest)wr).getTestResult();
            row[5]=result==null ? "waiting" : result;
            model.addRow(row);
        }
        }

