    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        Employee employee = (Employee) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();
        if(userName.equals("") || password.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty");
            return;
        }
        
        for (Network n : system.getNetworkList()) {
            for (State sn : n.getStateList()) {
                for (City cn : sn.getCityList()) {
                    for (Enterprise e : cn.getEnterpriseDir().getEnterpriseList()) {
                        for (UserAccount ua1 : e.getUserAccountDir().getUserAccountList()) {
                            if (ua1.getUsername().equals(userName)) {
                                JOptionPane.showMessageDialog(null, "User already exists");
                                return;

                            } else {
                                for (Organization o : e.getOrganizationDir().getOrganizationList()) {
                                    for (UserAccount ua2 : o.getUserAccountDir().getUserAccountList()) {
                                        if (ua2.getUsername().equals(userName)) {
                                            JOptionPane.showMessageDialog(null, "User already exists");
                                            return;
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
        organization.getUserAccountDir().createUserAccount(userName, password, employee, role);
        
        popData();
    }//GEN-LAST:event_createUserJButtonActionPerformed

